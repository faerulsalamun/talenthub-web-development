<?php

$harga_satu = 10;
$harga_dua = '10';

// Comparison ==
var_dump($harga_satu == $harga_dua);
echo "10 == 10 <br/>";

// Comparison ===
var_dump($harga_satu === $harga_dua);
echo "10 === 10<br/>";

// Comparison !=
var_dump($harga_satu != $harga_dua);
echo "10 != 10 <br/>";

// Comparison >
var_dump($harga_satu > $harga_dua);
echo "10 > 10 <br/>";

// Comparison >=
var_dump($harga_satu >= $harga_dua);
echo "10 >= 10<br/>";

// Comparison <
var_dump($harga_satu < $harga_dua);
echo "10 < 10<br/>";

// Comparison <=
var_dump($harga_satu <= $harga_dua);
echo "10 <= 10<br/>";
