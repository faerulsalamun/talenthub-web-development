<?php

$harga_satu = 10;
$harga_dua = 20;

// Penjumlahan
$jumlah = $harga_satu + $harga_dua;

echo $jumlah.'<br/>';

// Pengurangan
$pengurangan = $harga_dua - $harga_satu;

echo $pengurangan.'<br/>';

// Pengkalian
$pengkalian = $harga_satu * $harga_dua;

echo "$pengkalian <br/>";

// Pembagian
$pembagian = $harga_dua / $harga_satu;

echo "$pembagian <br/>";

// Modulus
$modulus = $harga_dua % $harga_satu;

echo "$modulus <br/>";